export const getCountryByCode = (code) => {
  return countries.find(country => country.code === code);
};

export const getCountryByDialCode = (dialCode) => {
  return countries.find(country => country.dialCode === dialCode);
};

export const getCurrencyByCountryCode = (countryCode) => {
  const country = getCountryByCode(countryCode);
  return country ? country.currency : null;
};

export const getBanksByCountryCode = (countryCode) => {
  const country = getCountryByCode(countryCode);
  return country ? country.banks : [];
};

export const getCountryByName = (name) => {
  return countries.find(country =>
    country.name.toLowerCase() === name.toLowerCase()
  );
};
